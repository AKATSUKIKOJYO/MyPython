x = int(input("x= "))
y = int(input("y= "))

a = x + y
s = x - y
m = x * y
avg = (x + y) / 2

max_number = max(x,y)
min_number = min(x,y)
print("두수의 합: ", a)
print("두수의 차: ", s)
print("두수의 곱: ", m)
print("두수의 평균: ", avg)
print("두수중 큰 수: ", max_number)
print("두수중 작은 수: ", min_number)
